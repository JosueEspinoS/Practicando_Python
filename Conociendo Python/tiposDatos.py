#Tipos de datos

#integers/Enteros
'''
Los números enteros son números sin partes decimales,
como -5, -4, -3, 0, 5, 7, etc.
Para declarar un número entero en Python, simplemente escriba
variableName = valor inicial
'''
age = 50
phone = 123456789
print("age =",age)
print("phone =",phone)
#float/ Coma flotante
'''
Flotante se refiere a números que tienen partes decimales,
como 1.234, -0.023, 12.01.
Para declarar un flotante en Python, escribimos
variableName = valor inicial
'''
weight = 50.35
height = 1.86
print("Weight =",weight)
print("Height =",height)

#String/ Cadena
'''
Para declarar una cadena, puede usar variableName = 'valor inicial' (comillas simples)
o variableName = "valor inicial" (comillas dobles)
'''
userName="Joel"
lastName='Herrera'
print(userName)
print(lastName)
# Podemos combinar múltiples subcadenas usando el signo de concatenación (+)
print(userName + lastName)
#Método upper() convierte la cadena a Mayusculas 
print(userName.upper())
