#Tupla
'''
son como listas, pero no puede modificar sus valores. Los valores iniciales son los valores que
permanecerán durante el resto del programa. Un ejemplo donde las tuplas son útiles es cuando su
programa necesita almacenar los nombres de los meses del año.

Para declarar una tupla, escribe tupleName = (valores iniciales). Observe que usamos corchetes ( )
cuando declaramos una tupla. Los valores múltiples están separados por una coma.

'''
meses=("enero","febrero","marzo","abril","mayo","junio","julio","agosto","septiembre","octubre","noviembre","diciembre")
print(meses)
print(meses[2])
print(meses[-1])
print(meses[-2])
print(meses[4:7])